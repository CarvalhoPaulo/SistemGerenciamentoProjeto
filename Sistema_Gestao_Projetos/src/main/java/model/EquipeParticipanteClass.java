package model;

public class EquipeParticipanteClass {

	private AtaClass ataClass;

	private TipoEquipeClass tipoEquipeClass;

	private PessoaClass pessoaClass;

	public AtaClass getAtaClass() {
		return ataClass;
	}

	public void setAtaClass(AtaClass ataClass) {
		this.ataClass = ataClass;
	}

	public TipoEquipeClass getTipoEquipeClass() {
		return tipoEquipeClass;
	}

	public void setTipoEquipeClass(TipoEquipeClass tipoEquipeClass) {
		this.tipoEquipeClass = tipoEquipeClass;
	}

	public PessoaClass getPessoaClass() {
		return pessoaClass;
	}

	public void setPessoaClass(PessoaClass pessoaClass) {
		this.pessoaClass = pessoaClass;
	}

}
