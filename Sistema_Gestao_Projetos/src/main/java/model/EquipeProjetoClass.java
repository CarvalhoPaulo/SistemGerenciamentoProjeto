package model;

public class EquipeProjetoClass {

	private FuncionarioClass funcionarioClass;

	private AtoresScrumClass atoresScrumClass;

	private ProjetoClass projetoClass;

	public FuncionarioClass getFuncionarioClass() {
		return funcionarioClass;
	}

	public void setFuncionarioClass(FuncionarioClass funcionarioClass) {
		this.funcionarioClass = funcionarioClass;
	}

	public AtoresScrumClass getAtoresScrumClass() {
		return atoresScrumClass;
	}

	public void setAtoresScrumClass(AtoresScrumClass atoresScrumClass) {
		this.atoresScrumClass = atoresScrumClass;
	}

	public ProjetoClass getProjetoClass() {
		return projetoClass;
	}

	public void setProjetoClass(ProjetoClass projetoClass) {
		this.projetoClass = projetoClass;
	}
}
